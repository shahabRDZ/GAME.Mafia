function getCountryOptions() {
  return Object.entries(COUNTRIES_DATA).map(([name, d]) =>
    `<option value="${name}">${d.flag} ${name}</option>`
  ).join('');
}

function getCityOptions(country) {
  const data = COUNTRIES_DATA[country];
  if (!data) return '<option value="">شهر انتخاب کنید</option>';
  return data.cities.map(c => `<option value="${c}">${c}</option>`).join('');
}
